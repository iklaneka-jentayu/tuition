// Language manager
let currentLanguage = localStorage.getItem('language') || 'en';

function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update values (for input fields)
    document.querySelectorAll('[data-i18n-value]').forEach(element => {
        const key = element.getAttribute('data-i18n-value');
        if (translations[lang] && translations[lang][key]) {
            element.value = translations[lang][key];
        }
    });
    
    // Update title attributes
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        if (translations[lang] && translations[lang][key]) {
            element.title = translations[lang][key];
        }
    });
    
    // Update alt attributes for images
    document.querySelectorAll('[data-i18n-alt]').forEach(element => {
        const key = element.getAttribute('data-i18n-alt');
        if (translations[lang] && translations[lang][key]) {
            element.alt = translations[lang][key];
        }
    });
    
    // Dispatch language change event for other components
    const event = new CustomEvent('languageChanged', { 
        detail: { language: lang, translations: translations[lang] } 
    });
    document.dispatchEvent(event);
    
    // Log language change
    logToSheet('info', `Language changed to ${lang}`, 'Language Selector');
}

// Initialize language selector
document.addEventListener('DOMContentLoaded', function() {
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
        languageSelect.addEventListener('change', function(e) {
            changeLanguage(e.target.value);
        });
    }
    
    // Apply initial language
    changeLanguage(currentLanguage);
});

// Helper function to get translation for a specific key
function __(key, lang = currentLanguage) {
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    // Fallback to English if key not found in selected language
    if (lang !== 'en' && translations.en && translations.en[key]) {
        return translations.en[key];
    }
    return key; // Return the key itself if no translation found
}

// Log function for CGS integration
function logToSheet(level, message, location, user = 'System') {
    console.log(`[${level}] ${location}: ${message} - ${user}`);
    
    // If Google Apps Script is configured, send log
    if (window.APP_SCRIPT_URL) {
        try {
            fetch(`${window.APP_SCRIPT_URL}?action=log`, {
                method: 'POST',
                mode: 'no-cors',
                headers: {
                    'Content-Type': 'application/json',
                },
                body: JSON.stringify({
                    level: level,
                    message: message,
                    location: location,
                    user: user,
                    timestamp: new Date().toISOString()
                })
            }).catch(err => console.error('Logging error:', err));
        } catch (err) {
            console.error('Failed to send log:', err);
        }
    }
}

